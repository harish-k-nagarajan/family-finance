function LoanTabs({ loans, selectedLoanId, onSelect }) {
  // Build tabs array with individual loans + combined tab
  const tabs = [
    ...loans.map(loan => ({
      id: loan.id,
      label: loan.loanName || 'Unnamed Loan',
      type: loan.loanType || 'other',
    })),
    {
      id: 'combined',
      label: 'All Loans',
      type: 'combined',
    },
  ];

  return (
    <div className="flex gap-1 p-1 rounded-lg glass-card overflow-x-auto">
      {tabs.map((tab) => (
        <button
          key={tab.id}
          onClick={() => onSelect(tab.id)}
          className={`relative px-4 py-2.5 text-sm font-medium rounded-md transition-colors flex items-center gap-2 whitespace-nowrap ${
            selectedLoanId === tab.id
              ? 'text-gray-900 dark:text-white'
              : 'text-gray-500 dark:text-gray-400'
          }`}
        >
          {selectedLoanId === tab.id && (
            <motion.div
              layoutId="activeLoanTab"
              className="absolute inset-0 bg-gradient-to-r from-teal-500/30 to-purple-500/30 rounded-md"
              transition={{ duration: 0.25, ease: 'easeOut' }}
            />
          )}
          <span className="relative z-10 flex items-center gap-2">
            {tab.type === 'combined' ? (
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5H7a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2" />
              </svg>
            ) : (
              <LoanTypeIcon type={tab.type} />
            )}
            {tab.label}
          </span>
        </button>
      ))}
    </div>
  );
}

export default LoanTabs;
